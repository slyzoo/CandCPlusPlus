#include <iostream>
int main(){

// tabs | \t
  std::cout << "\t" << "test" << '\n';

  // newline | \n
  std::cout << "test" << '\n';

  // double quotes | \"
  std::cout << "\"" << "test" << '\n';

  // single quotes | \'
  std::cout << "\'t" << "test" << '\n';

  // back slash | //
  std::cout << "\\" << "test" << '\n';

return 0;
}